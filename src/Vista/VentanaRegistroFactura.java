    private void cargarCombos() {
        javax.swing.DefaultComboBoxModel modelCli = new javax.swing.DefaultComboBoxModel();
        for (Hotel.DTO.Cliente c : clienteDao.listar()) {
            modelCli.addElement(c);
        }
        cbxCliente.setModel(modelCli);
        cbxEmpleado.removeAllItems(); 
        listaEmpleadosCombo.clear();  
        for (Hotel.DTO.Empleado e : empleadoDao.leerTodos()) {
            String texto = e.getNombre() + " " + e.getApellido() + " (" + e.getCargo() + ")";
            cbxEmpleado.addItem(texto);
            listaEmpleadosCombo.add(e);
        }
        cbxHabitacion.removeAllItems();
        listaHabitacionesCombo.clear();

        java.util.List<Hotel.DTO.Habitacion> habitaciones = habitacionDao.leerHabitaciones();
        
        for (Hotel.DTO.Habitacion h : habitaciones) {
            if(h.getEstado().equalsIgnoreCase("Disponible")){
                String texto = "Hab: " + h.getNumeroHabitacion() + " - $" + h.getPrecioxNoche();
                cbxHabitacion.addItem(texto);
                listaHabitacionesCombo.add(h);
            }
        }
    }
