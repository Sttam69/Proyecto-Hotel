    private void btnAgregarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarServicioActionPerformed
        // TODO add your handling code here:
        ArrayList<ServicioAdicional> disponibles = servicioDao.leer();
        
        if (disponibles.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay servicios registrados.");
            return;
        }
        Object seleccion = JOptionPane.showInputDialog(
                this, "Seleccione servicio:", "Agregar",
                JOptionPane.QUESTION_MESSAGE, null,
                disponibles.toArray(), disponibles.get(0)
        );
        if (seleccion != null) {
            ServicioAdicional serv = (ServicioAdicional) seleccion;
            serviciosSeleccionados.add(serv);
            DefaultTableModel model = (DefaultTableModel) tblServicios.getModel();
            model.addRow(new Object[]{
                serv.getCodigoServicio(), 
                serv.getNombre(),     
                serv.getCategoria(),    
                serv.getDescripcion(),    
                serv.getPrecio()        
            });
            
            totalVisual += serv.getPrecio();
        }
    }//GEN-LAST:event_btnAgregarServicioActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNumFactura.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Falta el número de factura.");
                return;
            }
            
            Cliente cli = (Cliente) cbxCliente.getSelectedItem();
            int indiceHab = cbxHabitacion.getSelectedIndex();
                 if (indiceHab == -1) {
                    JOptionPane.showMessageDialog(this, "Seleccione una habitación.");
            return;
            }
            Habitacion hab = listaHabitacionesCombo.get(indiceHab);
            int indiceEmp = cbxEmpleado.getSelectedIndex();
                if (indiceEmp == -1) {
                JOptionPane.showMessageDialog(this, "Seleccione un empleado.");
            return;
            }
            Empleado emp = listaEmpleadosCombo.get(indiceEmp);
            
            Hotel.DTO.Factura f = new Hotel.DTO.Factura();
            f.setNumeroFacturas(txtNumFactura.getText());
            f.setFecha("11/02/2026"); 
            f.setClienteAsociado(cli);
            f.setHabitacionesAsocionadas(hab);
            f.setEmpleadoAsociado(emp); 
            f.setServiceAdicional(serviciosSeleccionados);
            f.calcularTotal(); 
            if (facturaDao.registrar(f)) {

                hab.setEstado("Ocupada");
                JOptionPane.showMessageDialog(this, "Factura Generada.\nTotal a Pagar: $" + f.getPrecioTotal());
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "El número de factura ya existe.");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

