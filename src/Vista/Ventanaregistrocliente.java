    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        String codigo = txtCodigo.getText();

        if (cedula.isEmpty() || nombre.isEmpty() || codigo.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos.");
            return;
        }

        Hotel.DTO.Cliente nuevoCliente = new Hotel.DTO.Cliente(
                cedula,
                nombre,
                apellido,
                telefono,
                correo,
                codigo,
                "Ecuatoriana",
                false,
                "Efectivo"
        );

        Hotel.DAO.ClienteDAO dao = new Hotel.DAO.ClienteDAO();

        if (dao.registrar(nuevoCliente)) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Cliente guardado con éxito!");

            txtCedula.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtTelefono.setText("");
            txtCorreo.setText("");
            txtCodigo.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: La cédula ya existe o hubo un problema al guardar.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

