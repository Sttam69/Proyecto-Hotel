    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tblFacturas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una factura para eliminar.");
            return;
        }
        String numFactura = tblFacturas.getValueAt(fila, 0).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, 
                "¿Seguro que desea eliminar la factura N° " + numFactura + "?", 
                "Confirmar", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            try {
                facturaDao.eliminar(numFactura); 
                cargarTabla("");
                JOptionPane.showMessageDialog(this, "Eliminado correctamente.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

