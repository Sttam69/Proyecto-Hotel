    private void cargarTabla(String filtro) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("N° Factura");
        model.addColumn("Cliente");
        model.addColumn("Fecha");
        model.addColumn("Habitación");
        model.addColumn("Total");

        List<Factura> lista = facturaDao.listar();
        for (Factura f : lista) {

            String nombreCliente = (f.getClienteAsociado() != null) ? f.getClienteAsociado().getNombre() : "S/C";
            String numFactura = f.getNumeroFacturas();
            if (filtro.isEmpty() || 
                nombreCliente.toLowerCase().contains(filtro.toLowerCase()) || 
                numFactura.contains(filtro)) {

                model.addRow(new Object[]{
                    numFactura,
                    nombreCliente,
                    f.getFecha(),
                    (f.getHabitacionesAsociadas() != null) ? f.getHabitacionesAsociadas().getNumeroHabitacion() : "N/A",
                    "$" + f.getPrecioTotal()
                });
            }
        }
        tblFacturas.setModel(model);
    }
