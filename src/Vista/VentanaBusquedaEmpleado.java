    private void actualizarTabla() {
    try {
        Hotel.DAO.EmpleadoDAO dao = new Hotel.DAO.EmpleadoDAO();
        
        java.util.ArrayList<Hotel.DTO.Empleado> lista = dao.leerTodos();
        
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblEmpleados.getModel();
        
        modelo.setRowCount(0);

        for (Hotel.DTO.Empleado emp : lista) {
            Object[] fila = {
                emp.getCedula(),
                emp.getApellido(),
                emp.getNombre(),
                emp.getCargo(),
                emp.getTurno(),
                emp.getSueldo()
            };
            modelo.addRow(fila);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar los datos: " + e.getMessage());
    }
}
    
