    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String apellidoBusqueda = txtBusquedaApellido.getText().trim().toLowerCase();

    if (apellidoBusqueda.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese un apellido para buscar.");
        return;
    }

    try {
        Hotel.DAO.EmpleadoDAO dao = new Hotel.DAO.EmpleadoDAO();
        java.util.ArrayList<Hotel.DTO.Empleado> listaCompleta = dao.leerTodos();
        
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblEmpleados.getModel();
        modelo.setRowCount(0); // Limpiamos la tabla para mostrar solo resultados

        boolean encontrado = false;
        for (Hotel.DTO.Empleado emp : listaCompleta) {
            if (emp.getApellido().toLowerCase().contains(apellidoBusqueda)) {
                Object[] fila = {
                    emp.getCedula(),
                    emp.getApellido(),
                    emp.getNombre(),
                    emp.getCargo(),
                    emp.getTurno(),
                    emp.getSueldo()
                };
                modelo.addRow(fila);
                encontrado = true;
            }
        }

        if (!encontrado) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron empleados con el apellido: " + apellidoBusqueda);
            actualizarTabla(); 
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error durante la búsqueda: " + e.getMessage());
    }

    }//GEN-LAST:event_btnBuscarActionPerformed

