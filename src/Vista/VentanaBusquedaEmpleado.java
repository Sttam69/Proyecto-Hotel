    private void btnDespedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDespedirActionPerformed
        // TODO add your handling code here:
        int fila = tblEmpleados.getSelectedRow();

    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione al empleado que desea despedir.");
        return;
    }

    String cedula = tblEmpleados.getValueAt(fila, 0).toString();
    String apellido = tblEmpleados.getValueAt(fila, 1).toString();

    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de que desea despedir al empleado " + apellido + " con cedula " + cedula + "?", 
            "Confirmar Despido", 
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
        try {
            Hotel.DAO.EmpleadoDAO dao = new Hotel.DAO.EmpleadoDAO();
            
            java.util.ArrayList<Hotel.DTO.Empleado> lista = dao.leerTodos();

            lista.removeIf(emp -> emp.getCedula().equals(cedula));

            dao.guardarLista(lista);

            actualizarTabla();

            javax.swing.JOptionPane.showMessageDialog(this, "El empleado ha sido eliminado del sistema exitosamente.");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al procesar el despido: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDespedirActionPerformed

