    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        int fila = tbh.getSelectedRow();

    if(fila==-1){
        JOptionPane.showMessageDialog(this,"Seleccione una fila");
        return;
    }

    int numero = (int) tbh.getValueAt(fila,0);

    HabitacionDAO dao = new HabitacionDAO();
    List<Habitacion> lista = dao.leerHabitaciones();

    lista.removeIf(h -> h.getNumeroHabitacion()==numero);

    dao.escribirHabitaciones(lista);

    JOptionPane.showMessageDialog(this,"Eliminado");
    cargarTabla("");
    }//GEN-LAST:event_btneliminarActionPerformed

