    private void cargarTabla(String filtro) {

        HabitacionDAO dao = new HabitacionDAO();
        List<Habitacion> lista = dao.leerHabitaciones();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{
            "Numero", "Precio", "Estado", "Piso", "Tipo"
        });

        for (Habitacion h : lista) {

            if (filtro.isEmpty()
                    || h.getEstado().toLowerCase().contains(filtro.toLowerCase())
                    || h.getTiposHabitacion().toLowerCase().contains(filtro.toLowerCase())) {

                modelo.addRow(new Object[]{
                    h.getNumeroHabitacion(),
                    h.getPrecioxNoche(),
                    h.getEstado(),
                    h.getPiso(),
                    h.getTiposHabitacion()
                });
            }
        }

        tbh.setModel(modelo);
    }

