    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tblServicios.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila para eliminar.");
            return;
        }

        String codigo = tblServicios.getValueAt(fila, 0).toString();
        String nombre = tblServicios.getValueAt(fila, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Seguro que desea eliminar el servicio: " + nombre + "?",
                "Confirmar Eliminación",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            ServicioAdicionalDAO dao = new ServicioAdicionalDAO();
            String res = dao.eliminar(codigo);

            JOptionPane.showMessageDialog(this, res);
            cargarDatosATabla("");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

