    private void cargarDatosATabla(String filtro) {
        try {
            DefaultTableModel modelo = (DefaultTableModel) tblServicios.getModel();
            modelo.setRowCount(0);

            ServicioAdicionalDAO dao = new ServicioAdicionalDAO();
            ArrayList<ServicioAdicional> lista;

            if (filtro.isEmpty()) {
                lista = dao.leer();
            } else {
                lista = dao.buscar(filtro);
            }

            for (ServicioAdicional s : lista) {
                modelo.addRow(new Object[]{
                    s.getCodigoServicio(),
                    s.getNombre(),
                    s.getCategoria(),
                    s.getPrecio(),
                    s.getDescripcion()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos: " + e.getMessage());
        }
    }

